package com.softwareEng.Daycare;

public class Supplier {
    String companyName;
    long id;
    String telNo1;
    String telNo2;
    String physicalAddress;
    String BoxAddress;

    public Supplier(String companyName, long id, String telNo1, String telNo2, String physicalAddress, String boxAddress) {
        this.companyName = companyName;
        this.id = id;
        this.telNo1 = telNo1;
        this.telNo2 = telNo2;
        this.physicalAddress = physicalAddress;
        this.BoxAddress = boxAddress;

    }


}
